# ARQUIVO DE FUNÇÃO DE 1000 LINHAS



def soma(n1,n2):
    resultado = n1 + n2
    return resultado

def multi(n1,n2):
    resultado = n1 * n2
    return resultado

def div(n1,n2): #Divisão
    resultado = n1 / n2
    return resultado

def subtracao(n1,n2):
    resultado = n1 - n2
    return resultado

def pot(n1,n2):     #Potenciação
    resultado = n1 ** n2
    return resultado

x = soma(10, 20)
y = multi(10,20)
z = pot(3,4)

print(x+y+z)

