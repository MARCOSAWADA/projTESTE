
#9. atraves dos inputs adicionar no dicionario
# texto.split() contador da lista dentro do dicionario

# Criando um dicionário vazio
informacoes = {}

# Solicitando informações do usuário
chave = input("Digite a chave para o dicionário: ")
valor = input("Digite o valor correspondente: ")

# Armazenando as informações no dicionário
informacoes[chave] = valor

# Exibindo o dicionário atualizado
print("Dicionário atualizado:", informacoes)
